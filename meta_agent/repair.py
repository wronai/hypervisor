from __future__ import annotations

import copy
import re
from pathlib import Path
from typing import Any

import yaml

from generator.validate import validate_agent
from meta_agent.models import RepairResult
from meta_agent.planner import package_name


def _load_yaml(path: Path) -> dict[str, Any]:
    raw = yaml.safe_load(path.read_text(encoding="utf-8"))
    if not isinstance(raw, dict):
        return {}
    return raw


def _write_yaml(path: Path, data: dict[str, Any]) -> None:
    path.write_text(yaml.safe_dump(data, sort_keys=False, allow_unicode=True), encoding="utf-8")


def repair_agent_spec(path: Path, *, write: bool = False) -> RepairResult:
    """Repair common YAML contract mistakes without inventing domain logic.

    It fixes missing agent metadata, duplicate capability names, missing renderer for
    reads, missing schemas when they can be inferred, and package naming issues.
    """

    errors_before = validate_agent(path)
    warnings: list[str] = []
    data = _load_yaml(path)
    repaired = copy.deepcopy(data)

    agent = repaired.setdefault("agent", {})
    if not agent.get("name"):
        agent["name"] = path.stem.replace("_", "-")
        warnings.append("Filled missing agent.name from filename.")
    if not agent.get("python_package"):
        agent["python_package"] = package_name(str(agent["name"]))
        warnings.append("Filled missing agent.python_package.")
    if not agent.get("version"):
        agent["version"] = "0.1.0"
        warnings.append("Filled missing agent.version.")
    if not agent.get("runtime_url_env"):
        agent["runtime_url_env"] = "RESOURCE_RUNTIME_URL"
    if not agent.get("runtime_url_default"):
        agent["runtime_url_default"] = "http://localhost:8000"

    capabilities = repaired.setdefault("capabilities", [])
    if capabilities is None:
        capabilities = []
        repaired["capabilities"] = capabilities

    seen: dict[str, int] = {}
    for cap in capabilities:
        if not isinstance(cap, dict):
            continue
        name = str(cap.get("name") or "capability")
        count = seen.get(name, 0)
        if count:
            cap["name"] = f"{name}_{count + 1}"
            warnings.append(f"Renamed duplicate capability {name} to {cap['name']}.")
        seen[name] = count + 1

        if not cap.get("type"):
            cap["type"] = "resource_read" if cap.get("uri") else "command"
            warnings.append(f"Inferred type for capability {cap.get('name')}.")

        if cap.get("type") == "resource_read":
            if not cap.get("renderer"):
                uri = str(cap.get("uri") or "")
                cap["renderer"] = "timeline" if any(part in uri for part in ["/events", "/history", "/activity"]) else "detail"
                warnings.append(f"Filled renderer for {cap.get('name')}.")
            if not cap.get("output_schema") and cap.get("uri"):
                domain = re.sub(r"[^a-zA-Z0-9_]", "", str(cap["uri"]).split("//", 1)[-1].split("/", 1)[0]) or "resource"
                cap["output_schema"] = f"app.{domain}.v1.AutoGeneratedView"
                warnings.append(f"Filled output_schema for {cap.get('name')}.")
        elif cap.get("type") == "command":
            if not cap.get("command"):
                cap["command"] = str(cap.get("name") or "GeneratedCommand").title().replace("_", "")
                warnings.append(f"Filled command for {cap.get('name')}.")
            if not cap.get("input_schema"):
                cap["input_schema"] = f"app.commands.v1.{cap['command']}Command"
                warnings.append(f"Filled input_schema for {cap.get('name')}.")

    if write:
        _write_yaml(path, repaired)
        errors_after = validate_agent(path)
    else:
        tmp = path.with_suffix(path.suffix + ".repair.tmp")
        try:
            _write_yaml(tmp, repaired)
            errors_after = validate_agent(tmp)
        finally:
            tmp.unlink(missing_ok=True)

    return RepairResult(
        changed=repaired != data,
        errors_before=errors_before,
        errors_after=errors_after,
        warnings=warnings,
        repaired_yaml=repaired,
    )
